# What are variables in Python?
# Variables in Python are used to store data values. They act as containers for data that can be referenced and manipulated in a program. Variables can hold different types of data, such as numbers, strings, lists, and more.
# Variables are created by assigning a value to a name using the assignment operator `=`. The  name on the left side of the operator is the variable name, and the value on the right side is the data being stored in the variable.
# Variables can be reassigned to different data types, and their values can be modified throughout the program.
# Variables are case-sensitive, meaning `myVariable` and `myvariable` are treated as different variables.
# Variables can be used in expressions and operations to perform calculations or manipulate data.

# Examples of variables
my_variable = 10
my_string = "Hello, World!"
my_list = [1, 2, 3, 4, 5]

# Modifying variable values
my_variable = my_variable + 5
my_string = my_string + " How are you?"
my_list.append(6)

print(my_variable)  # Output: 15
print(my_string)  # Output: Hello, World! How are you?
print(my_list)  # Output: [1, 2, 3, 4, 5, 6]

