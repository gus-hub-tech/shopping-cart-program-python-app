# Assignment Operators and their Usage.
# Assignment operators are used to assign values to variables.
# They are used to assign a value to a variable, and can also be used to perform arithmetic operations on the value being assigned.

# The = operator is used to assign a value to a variable.

x = 10
y = 5
print(x) # value 10 assigned to x
print(y) # value 5 assigned to y
