# Built-in Modules 

# This code imports the math module and prints the value of pi (Ï€)
 
import math # Importing the math module
print(math.pi)  # Output: 3.141592653589793

# This code imports the random module and prints a random integer between 1 and 10

import random  # Importing the random module
print(random.randint(1, 10))  # Output: Random integer between 1 and 10

# This code imports the datetime module and prints the current date and time

import datetime  # Importing the datetime module
print(datetime.datetime.now())  # Output: Current date and time

# This code imports the os module and prints the current working directory
# The os module provides a way of using operating system dependent functionality like reading or writing to the file system.

import os  # Importing the os module
print(os.getcwd())  # Output: Current working directory

# This code imports the sys module and prints the Python version information

import sys  # Importing the sys module
print(sys.version)  # Output: Python version information