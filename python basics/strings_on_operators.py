# What are string operators in Python?
# String operators in Python are special symbols or keywords that are used to perform operations on strings. These operations can include concatenation, repetition, and membership testing.

# Examples of string operators

# Concatenation operator (+)
string1 = "Hello"
string2 = "World"
result = string1 + " " + string2
print(result)  # Output: Hello World

# Repetition operator (*)
string3 = "Hello"
result = string3 * 3
print(result)  # Output: HelloHelloHello

# Membership operator (in)
string4 = "Hello, World!"
print("Hello" in string4)  # Output: True
print("Python" in string4)  # Output: False
print("!" in string4)  # Output: True