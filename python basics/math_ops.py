# Math operations - Examples that will be used and imported in other files like the modules.py file

def add(a, b): # This function takes two numbers as arguments and returns their sum.
    return a + b # Returns the sum of the two numbers.

def subtract(a, b): # This function takes two numbers as arguments and returns their difference.
    return a - b # Returns the difference of the two numbers.

def multiply(a, b): # This function takes two numbers as arguments and returns their product.
    return a * b # Returns the product of the two numbers.

