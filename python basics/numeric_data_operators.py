# Numeric data operators with Python

# Arithmetic Operator types

# The + operator is used to add two numbers.
x = 20
y = 20
print(x + y)

# The - operator is used to subtract one number from another.

print(x - y)

# The * operator is used to repeat a string a specified number of times.

print(x * y)

# The / operator is used to divide one number by another.

print(x / y)

# The % operator used to find the remainder of the division of one number by another. [a.k.a -modulus]

print(x % y)

# The ** operator is used to raise a number to the power of another number. [a.k.a -exponentiation]

print(x ** y)

