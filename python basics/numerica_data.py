# What is numeric data in Python?
# Numeric data in Python refers to data types that represent numbers. Python supports several numeric types, including integers, floating-point numbers, and complex numbers.
# Numeric data is used for various operations such as arithmetic calculations, comparisons, and mathematical functions.

# Examples of numeric data types
integer_number = 10 # Integer type has no decimal point
float_number = 3.14 # Floating-point type has a decimal point
complex_number = 2 + 3j # Complex type has a real and an imaginary part

# Performing arithmetic operations
result = integer_number + float_number
print(result)  # Output: 13.14

# Comparing numeric values
print(integer_number > float_number)  # Output: True

# Mathematical functions
print(abs(-5))  # Output: 5 (absolute value)
print(pow(2, 3))  # Output: 8 (2 raised to the power of 3)
print(round(3.14159, 2))  # Output: 3.14 (rounded to 2 decimal places)